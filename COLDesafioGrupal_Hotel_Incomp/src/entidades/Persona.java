package entidades;

/**
 *
 * @author Pc
 */
public class Persona {
    
    private String nombre;
    private int edad;
    private long DNI;
    private String pais;

    public Persona(String nombre, int edad, long DNI, String pais) {
        this.nombre = nombre;
        this.edad = edad;
        this.DNI = DNI;
        this.pais = pais;
    }

    public Persona() {
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public int getEdad() {
        return edad;
    }

    public void setEdad(int edad) {
        this.edad = edad;
    }

    public long getDNI() {
        return DNI;
    }

    public void setDNI(long DNI) {
        this.DNI = DNI;
    }

    public String getPais() {
        return pais;
    }

    public void setPais(String pais) {
        this.pais = pais;
    }
    
}
